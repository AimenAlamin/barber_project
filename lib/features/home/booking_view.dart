          shopName: "The Gentleman's Den",
          address: '123 Main Street, Anytown, USA',
          services: [
            'Undercut Haircut',
            'Regular Shaving',
            'Natural Hair Wash',
          ],
          imageUrl: 'assets/images/ImageBarber.png',
        ),
      ],
    );
  }
}

